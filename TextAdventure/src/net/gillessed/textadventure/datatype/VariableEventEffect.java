package net.gillessed.textadventure.datatype;

public class VariableEventEffect extends EventEffect<Variable> {
	private static final long serialVersionUID = -5445363013970622076L;

	private String expression;
	
	public VariableEventEffect(Event parent) {
		super(null, parent);
	}

	public String getExpression() {
		return expression;
	}

	public void setExpression(String expression) {
		this.expression = expression;
	}
}
